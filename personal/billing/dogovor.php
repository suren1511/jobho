        1. Предмет договора<br>
        1.1. Принципал предоставляет услуги по размещению Клиентов направляемых Агентом, в общежитиях (хостелах, гостиницах, доходных домах, квартирах) которые принадлежат ему на правах собственности или аренды. Перечень объектов, их адреса, условия проживания и цены, по которым производятся взаиморасчёты между Сторонами, указаны в Приложении №1 к настоящему Договору.
        <br>
        1.2. Агент обязуется по поручению и в интересах Принципала совершать действия по поиску и привлечению Клиентов, заинтересованных в услугах Принципала, а Принципал обязуется принять от Агента результаты совершенных им действий и уплатить вознаграждение в размере, сроке и порядке, установленных настоящим Договором.
        <br>
        <br>

        2. Права и обязанности сторон
        <br>
        2.1. Общие положения:
        <br>
        2.1.1. В целях выполнения обязательств по данному договору  Агент направляет Клиентов, желающих получить услугу по временному размещению и проживанию, на объекты Принципала. Права по заключению договоров на оказание услуг, и обязательства по ним, возникают непосредственно у Принципала.
        <br>
        2.2. Принципал вправе:
        <br>
        2.2.1. Давать Агенту указания относительно условий представления его интересов Агентом. При этом такие указания должны быть своевременными, правомерными, осуществимыми и не противоречить данному договору.
        <br>
        2.2.2.  При наличии возражений по отчету Агента, сообщать о них Агенту в течение трёх  дней со дня получения отчета. При отсутствии возражений отчет считается принятым.
        <br>
        2.2.3. Размещать рекламные  материалы на информационных ресурсах Агента (www.jobhostel.ru), в случае если ресурс предполагает размещение таких материалов.
        <br>
        2.2.4. Воспользоваться VIP-программой для приоритетного подхода к продаже его услуг.
        <br>
        2.2.5. В одностороннем порядке изменить действующие тарифы и условия предоставления услуг, уведомив об этом Агента не менее чем за три рабочих дня до введения изменений.
        <br>
        2.2.6. Отказаться от размещения Клиента, направленного Агентом, в обязательном порядке проинформировав Агента о причинах отказа.
        <br>
        2.2.7. Перейти на другую Программу со следующего календарного месяца, предварительно уведомив об этом Агента не менее чем за пять рабочих дня.
        <br>
        2.3. Агент вправе:
        <br>
        2.3.1. Направлять Клиентов в общежития Принципала, для их осмотра.
        <br>
        2.3.2. При отсутствии конкретных указаний и рекомендаций Принципала по поводу возможных действий Агента в той или иной ситуации, действовать в соответствии с обычаями делового оборота и  целями данного Договора.
        <br>
        2.3.3. Требовать от Принципала выплаты вознаграждения за услуги, оказанные ему по настоящему Договору.
        <br>
        2.4. Принципал обязуется:
        <br>
        2.4.1.  Обеспечить  высокое качество обслуживания Клиентов, направленных Агентом.
        <br>
        2.4.2. Своевременно и в полном объеме выплачивать Агенту вознаграждение за совершение им в интересах Принципала действий, в размере и порядке, установленных в разделе 4  настоящего Договора.
        <br>
        2.4.3. В течение срока действия Договора предоставлять Агенту информацию об услугах  составляющих предмет Договора -  прейскуранты, фотографии, рекламные материалы и т.д.
        <br>
        2.4.4. Ущерб, причиненный Клиенту по вине Принципала, либо Принципалу по вине Клиента, возмещается сторонами инцидента самостоятельно в полном объеме, без участия Агента.
        <br>
        2.4.5. Информировать Агента о факте подписания договора с Клиентом (или иным лицом, связанным с Клиентом) или о начале фактического оказания услуг ему или аффилированным с ним лицам.
        <br>
        2.4.6. Принципал обязуется не передавать информацию о Клиентах, направленных Агентом, третьим лицам. В противном случае, Принципал обязуется оплатить Агенту штраф в десяти кратном размере от расчётной суммы агентского вознаграждения.
        <br>
        2.4.7. Если Принципал уже работает с Клиентом указанным в Заявке, то он должен оповестить об этом Агента в течение десяти минут с момента получения Заявки. В противном случае он обязуется оплатить услуги Агента в безусловном порядке, вне зависимости от того работает он с этим Клиентом или нет.
        <br>
        2.5. Агент обязуется:
        <br>
        2.5.1. Самостоятельно и с привлечением третьих лиц, осуществлять поиск потенциальных Клиентов с целью удовлетворения их потребностей в услугах, предоставляемых  Принципалом.
        <br>
        2.5.2. Направлять Принципалу Заявки  на размещение Клиентов. Форма Заявки определена в Приложении №2 к настоящему Договору.
        <br>
        2.5.3. В Заявке указывать предполагаемые даты начала и завершения периода предоставления услуг Принципалом, объём запрашиваемых услуг и контактные данные Клиента.
        <br>
        2.5.4. Предоставлять Принципалу сведения об аннуляции или изменении Заявок Клиента по телефону или электронной почте.
        <br>
        2.5.5. При поиске потенциальных Клиентов, руководствоваться указаниями Принципала в отношении цен, условий оказания услуг и их оплаты, которые определены в Приложении №1.
        <br>
        <br>

        VIP-программа
        <br>
        3.1. С целью увеличения объёма продаж услуг Принципала, он может в любой момент перейти на обслуживание по  VIP–программе. При этом  подписывается доп. соглашение.
        <br>
        3.2. В этом случае  Агент направляет Клиентов на объекты Принципала в приоритетном порядке по отношению к другим Принципалам, и объектам в этом районе, для скорейшего  достижения  и поддержки высокой загрузки его объектов.
        <br>
        3.3. В период действия VIP-программы вознаграждение Агента составляет 35% от фактической стоимости каждого койка-места  за период в тридцать календарных дней с момента заселения. В случае если услуги Принципалом оказаны Клиенту на меньший срок, то вознаграждение Агента оплачивается из расчёта фактически оказанных услуг.
        <br>
        <br>

        Порядок расчетов
        <br>
        4.1. Клиенты, направленные Агентом, оплачивают услуги Принципала, по безналичному или наличному расчету, согласно договору, заключенному между Принципалом и Клиентом, по тарифам Принципала.
        <br>
        4.2. В первый рабочий день нового календарного месяца следующего за отчётным, Агент предоставляет «Отчет агент» (Приложение №3) в котором  указывает статистические данные по  продаже услуг Принципала в предыдущем месяце.
        <br>
        4.3. Принципал обязан согласовать и подписать «Отчет агента» в течение трёх рабочих дней. Если к указанному сроку «Отчет агент» не будет получен Агентом, и Принципал не предоставит аргументированных возражений, то односторонне подписанный Агентом «Отчет агента» считается подтверждением надлежащего оказания услуг по данному Договору.
        <br>
        4.4. После подписания «Отчета агента», Стороны производят окончательный расчёт по задолженностям, не позднее 10-го числа месяца, следующего за отчётным.
        <br>
        4.5. Вознаграждение Агента составляет 25% от фактической стоимости всех койка-мест  оплаченных Клиентом за расчётный период в тридцать календарных дней с момента заселения.
        <br>
        Выплата вознаграждения Агенту производится Принципалом двумя платежами:
        <br>
        -  первый в течение трёх банковских дней с момент получения денежных средств от Клиента;
        <br>
        - второй в течение трёх банковских дней с момента проведения сверки фактически оказанных услуг;
        <br>
        Если Клиент прожил менее тридцати календарных дней, то вознаграждение Агента оплачивается из расчёта фактически оказанных услуг.
        <br>
        4.6. При увеличении Клиентом объёма арендуемых койка-мест в расчётный период, Принципал уведомляет об этом Агента, и выплачивает вознаграждение согласно установленных тарифов, за каждое дополнительное койка-место от первоначально заявленных мест, указанных в Заявке (Приложении №2) к настоящему Договору.
        <br>
        4.7. В случае просрочки оплаты вознаграждения по настоящему Договору, Агент взыскивает с Принципала пеню в размере 0,5 % от установленной суммы оплаты, за каждый день просрочки до момента полного погашения задолженности.
        <br>
        4.8. В случае возникновения дебиторской задолженности со стороны Принципала, Агент приостанавливает направление Клиентов на его объекты, до полной оплаты задолженности.
        <br>
        <br>

        5. Ответственность сторон
        <br>
        5.1. В случае неисполнения полностью или частично обязательств по настоящему Договору, стороны несут ответственность, в соответствии с действующим законодательством Российской Федерации.
        <br>
        5.1. Стороны освобождаются от ответственности за частичное или полное неисполнение обязательств по настоящему Договору, если это неисполнение явилось следствием обстоятельств непреодолимой силы, возникших после заключения настоящего Договора.
        <br>
        5.1. Споры, которые могут возникнуть при исполнении условий настоящего Договора, по возможности, решаются путем проведения переговоров.
        <br>
        5.2. При не достижении согласия или взаимоприемлемого решения Стороны вправе решить спор в судебном порядке в соответствии с действующим законодательством РФ.
        <br>
        <br>

        6. Изменение Договора
        <br>
        6.1. Настоящий Договор может быть изменен в любой период его действия на основе взаимного согласия сторон и наличия объективных причин, вызвавших такие действия.
        <br>
        6.2. Любые договоренности сторон по изменению условий настоящего Договора имеют силу в том случае, если они оформлены в письменном виде, подписаны обеими сторонами и скреплены их печатями.
        <br>
        <br>

        7. Расторжение Договора
        <br>
        7.1. Настоящий Договор может быть расторгнут в любой момент по соглашению сторон или в одностороннем порядке любой из сторон.
        <br>
        7.2. Сторона, решившая расторгнуть Договор, обязана уведомить об этом другую сторону в письменном виде не позднее, чем за пятнадцать календарных дней до даты расторжения.
        <br>
        7.3. В случае расторжения Договора по любому из оснований, стороны обязаны провести полный взаиморасчет и исполнить обязательства, возникшие до момента расторжения договора, в полном объеме.
        <br>
        <br>

        8. Срок действия Договора
        <br>
        8.1. Настоящий Договор вступает в силу с момента подписания и действует бессрочно.
        <br>
        <br>

        9. Прочие условия
        <br>
        9.1. Настоящий Договор составлен в двух подлинных экземплярах, по одному для каждой из сторон, имеющих равную юридическую силу.
        <br>
        9.2 По настоящему Договору законные проценты (п. 1 ст. 317.1 ГК РФ) не начисляются и не уплачиваются, в том числе:
        <br>
        - Принципал не начисляет, а Агент не уплачивает законные проценты на сумму аванса, перечисленного Принципалом Агенту;
        <br>
        - Агент не начисляет, а Принципал не уплачивает законные проценты на сумму задолженности по оплате оказанных услуг за период с даты подписания «Отчета агента» и  акта оказания услуг до момента оплаты услуг.
